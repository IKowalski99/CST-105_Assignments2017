public class FahrenheitToCelsius1 {
	public static void main (String[] args) {

		//Conversion for Fahrenheit to Celsius
		double f;
		double c;
		
		//Enter value for f (Fahrenheit)
		f = 47;
		
		//Convert Fahrenheit to Celsius
		c = (f - 32) * 5 / 9;
		
		//Print results
		System.out.println(f + " Fahrenheit is equivalent to " + c + " Celsius");
		
	}
}
